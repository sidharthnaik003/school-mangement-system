import { createSlice } from "@reduxjs/toolkit";

const initialState = {
    employeesList: [],
    loading: false,
    error: null,
    response: null,
    statestatus: "idle",
};

const employeeSlice = createSlice({
    name: 'employee',
    initialState,
    reducers: {
        getRequest: (state) => {
            state.loading = true;
        },
        stuffDone: (state) => {
            state.loading = false;
            state.error = null;
            state.response = null;
            state.statestatus = "added";
        },
        getSuccess: (state, action) => {
            state.employeesList = action.payload;
            state.loading = false;
            state.error = null;
            state.response = null;
        },
        getFailed: (state, action) => {
            state.response = action.payload;
            state.loading = false;
            state.error = null;
        },
        getError: (state, action) => {
            state.loading = false;
            state.error = action.payload;
        },
        underemployeeControl: (state) => {
            state.loading = false;
            state.response = null;
            state.error = null;
            state.statestatus = "idle";
        }
    },
});

export const {
    getRequest,
    getSuccess,
    getFailed,
    getError,
    underemployeeControl,
    stuffDone,
} = employeeSlice.actions;

export const employeeReducer = employeeSlice.reducer;